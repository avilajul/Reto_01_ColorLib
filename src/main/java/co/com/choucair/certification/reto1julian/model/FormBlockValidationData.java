package co.com.choucair.certification.reto1julian.model;

public class FormBlockValidationData {

    private String required;
    private String email;
    private String formPassword;
    private String formConfirmPassword;
    private String date;
    private String url;
    private String digitOnly;
    private String range;

    public String getRequired() {
        return required;
    }

    public String getEmail() {
        return email;
    }

    public String getFormPassword() {
        return formPassword;
    }

    public String getFormConfirmPassword() {
        return formConfirmPassword;
    }

    public String getDate() { return date; }

    public String getUrl() {
        return url;
    }

    public String getDigitOnly() {
        return digitOnly;
    }

    public String getRange() {
        return range;
    }


}
